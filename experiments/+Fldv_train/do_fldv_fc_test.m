function result = do_fldv_fc_test(conf, model_stage, imdb)
    errors                      = fldv_fc_test(conf, imdb, ...
                                        'net_def_file',     model_stage.test_net_def_file, ...
                                        'net_file',         model_stage.output_model_file, ...
